main() {
  // Arthmetic Operators ( + - * / % ~/ )
  var x = 5;
  var y = 2;
  print('$x + $y = ${(x + y)}');
  print('$x - $y = ${(x - y)}');
  print('$x * $y = ${(x * y)}');
  print('$x / $y = ${(x / y)}'); // result will be double
  print('$x ~/ $y = ${(x ~/ y)}'); // result will be integer
  print('$x % $y = ${(x % y)}'); // remainder / modulas
}

// result
// 5 + 2 = 7
// 5 - 2 = 3
// 5 * 2 = 10
// 5 / 2 = 2.5
// 5 ~/ 2 = 2
// 5 % 2 = 1
